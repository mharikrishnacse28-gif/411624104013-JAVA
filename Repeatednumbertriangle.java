public class Repeatednumbertriangle {
        public static void main(String[] args) {
        int i,j,n=6;
        for(i=1;i<=n;i++){
            for(j=1;j<=i;j++){
                if(j>1){
                    System.out. print(" ");
                }
                System.out.print(i+"");
            }
            System.out.println();
        }
    }
}
